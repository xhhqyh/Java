public enum Num {
	PRICE(0,5);
	private Integer no;
	private Integer num;
	Num(Integer no,Integer num){
		this.no=no;
		this.num=num;
	}
	public Integer getNo() {		
		return no;
	}
	public Integer getNum() {
		return num;
	}
}
